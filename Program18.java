//Increment ++ and Decrement -- Operator Overloading in Java Programming
package Javabatch;

public class Program18 {

	public static void main(String[] args) {
		 int a = 10;
	        int b = ++a;
	        System.out.println(b);//pre increment
	        b= b+1;
	        System.out.println(b);//post increment
	        int c= --b;
	        System.out.println(c);//pre decrement 
	        
	        c= c-1;
	        System.out.println(c);//post decrement
	        

	}

}
